import random

def augment_bounding_box(box, width, height, max_shift=4):
    """
    Централизованная геометрическая аугментация (Jittering) для одной рамки.
    Случайно сдвигает границы блока, имитируя неидеальное OCR-распознавание.
    """
    x1, y1, x2, y2 = box
    
    # Расчет случайного смещения по осям X и Y
    dx = random.randint(-max_shift, max_shift)
    dy = random.randint(-max_shift, max_shift)
    
    # Применяем сдвиг с жестким контролем границ изображения
    ax1 = max(0, min(width, x1 + dx))
    ay1 = max(0, min(height, y1 + dy))
    ax2 = max(0, min(width, x2 + dx))
    ay2 = max(0, min(height, y2 + dy))
    
    # Защита от вырожденных прямоугольников (координаты конца всегда больше начала)
    if ax2 <= ax1: ax2 = ax1 + 1
    if ay2 <= ay1: ay2 = ay1 + 1
    
    return [ax1, ay1, ax2, ay2]

def get_yolo_augmentation_params():
    """
    Глобальные настройки коэффициентов аугментации для движка Ultralytics YOLOv8.
    Позволяет централизованно управлять искажениями визуальной модальности.
    """
    return {
        "hsv_h": 0.015,     # Изменение оттенка цвета (имитация разного баланса белого)
        "degrees": 5.0,     # Случайный поворот скана (имитация кривой подачи в сканер)
        "translate": 0.1,   # Случайное линейное смещение всей страницы
        "scale": 0.1,       # Случайное масштабирование документа
        "perspective": 0.0, # Перспективные искажения (отключены для плоских бланков)
        "flipud": 0.0,      # Отражение по вертикали (недопустимо для текстов)
        "fliplr": 0.0       # Отражение по горизонтали (недопустимо для текстов)
    }
